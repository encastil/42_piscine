int	ft_str_is_alpha(char *str)
{
	int	x;

	x = 0;
	while ((97 <= str[x] && str[x] <= 122) || (65 <= str[x] && str[x] <= 90))
		x++;
	if (str[x] == '\0')
		return (1);
	else
		return (0);
}
