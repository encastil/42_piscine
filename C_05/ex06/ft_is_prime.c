int	ft_is_prime(int nb)
{
	int	z;

	if (nb <= 1)
		return (0);
	z = 2;
	while (z <= (nb / 2))
	{
		if (!(nb % z))
			return (0);
		else
			z++;
	}
	return (1);
}
